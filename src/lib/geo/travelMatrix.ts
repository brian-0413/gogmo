/** 查詢兩區之間的旅行時間（分鐘）。查不到回傳 null。 */
export function getTravelTime(from: string, to: string): number | null {
  const fromRow = TRAVEL_MATRIX[from as RegionId]
  if (!fromRow) return null
  return fromRow[to as RegionId] ?? null
}

/** 同區域（不需要行車） */
export function isSameRegion(a: string, b: string): boolean {
  return a === b
}

/** 兩區是否相鄰（行政區相鄰關係） */
export function isAdjacent(a: string, b: string): boolean {
  const adj = ADJACENT_REGIONS[a]
  return adj ? adj.includes(b) : false
}

/** 銜接延遲（分鐘）：現有行程結束 → 新訂單開始 */
export function calcGapMinutes(currentEndTime: Date, newOrderTime: Date): number {
  return Math.max(0, Math.floor((newOrderTime.getTime() - currentEndTime.getTime()) / 60000))
}

/**
 * 判斷候選訂單是否能銜接現有行程。
 * 銜接條件：
 * - 時間差 30 分鐘 ～ 180 分鐘（3小時）
 * - 行車時間 <= 時間差（緩衝足夠）
 * - 且行車時間 <= 90 分鐘（過長的不考慮）
 */
export function canConnect(
  currentEndTime: Date,
  currentDropoffRegion: string,
  newOrderPickupRegion: string,
  newOrderTime: Date
): boolean {
  const gap = calcGapMinutes(currentEndTime, newOrderTime)
  if (gap < 30 || gap > 180) return false

  const travel = getTravelTime(currentDropoffRegion, newOrderPickupRegion)
  if (travel === null) return false
  if (travel > gap) return false
  if (travel > 90) return false // 過長不考慮

  return true
}